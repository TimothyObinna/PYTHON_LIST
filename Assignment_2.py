# Task 2: 
# Write a Python program with 15 items in a list; Print the largest and smallest number from the list.

#ANSWER:
  
Numbers = [2,20,30,40,5,6,9,10,53,45,67,23,65,34,31]

# Max and Min are in-built function for identifying largest and smallest items in a list
Largest = max(Numbers)
Smallest = min(Numbers)
print(f"The largest and the Smallest in the list are {Largest} and {Smallest} respectively.")
